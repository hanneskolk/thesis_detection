# dataset.py - Dataset preparation for YOLO training
#
# Expects a raw dataset in one of these layouts:
#   A)  source_dir/images/*.jpg  +  source_dir/labels/*.txt   (already split)
#   B)  source_dir/images/*.jpg  +  source_dir/labels/*.txt   (flat, will be split)
#   C)  source_dir/*.jpg  +  source_dir/*.txt                 (everything in root)
#
# Produces:
#   dataset/
#     train/images/  train/labels/
#     valid/images/  valid/labels/
#     test/images/   test/labels/
#     data.yaml

import os
import shutil
import random
import argparse
from pathlib import Path


# ── Configuration ─────────────────────────────────────────────────────────────
DEFAULT_SOURCE    = "raw_dataset"     # folder with your downloaded/extracted data
DEFAULT_OUTPUT    = "dataset"         # where the prepared dataset will be written
TRAIN_RATIO       = 0.80
VAL_RATIO         = 0.15
# TEST_RATIO is the remainder: 1 - TRAIN - VAL = 0.05

# Class names — must match data.yaml index order exactly
CLASS_NAMES = [
    "Artillery",       # 0
    "Missile",         # 1
    "Radar",           # 2
    "RocketLauncher",  # 3
    "Soldier",         # 4
    "Tank",            # 5
    "Vehicle",         # 6
]
# ─────────────────────────────────────────────────────────────────────────────

IMG_EXTS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}


def find_pairs(source: Path) -> list[tuple[Path, Path]]:
    """
    Scan source directory and return verified (image, label) pairs.
    Searches in root, images/, and any nested images/ folder.
    """
    # Candidate image directories to check
    candidate_img_dirs = [source]
    for item in source.rglob("*"):
        if item.is_dir() and item.name.lower() in ("images", "imgs", "image"):
            candidate_img_dirs.append(item)

    pairs = []
    seen  = set()

    for img_dir in candidate_img_dirs:
        # Corresponding labels folder: sibling named 'labels'
        lbl_dir = img_dir.parent / "labels"
        if not lbl_dir.exists():
            lbl_dir = img_dir  # labels might be in the same folder

        for img_path in sorted(img_dir.iterdir()):
            if img_path.suffix.lower() not in IMG_EXTS:
                continue
            if img_path.stem in seen:
                continue

            lbl_path = lbl_dir / (img_path.stem + ".txt")
            if not lbl_path.exists():
                print(f"  ⚠️  No label for {img_path.name} — skipping")
                continue

            pairs.append((img_path, lbl_path))
            seen.add(img_path.stem)

    return pairs


def copy_split(pairs: list, output: Path, split: str) -> None:
    img_out = output / split / "images"
    lbl_out = output / split / "labels"
    img_out.mkdir(parents=True, exist_ok=True)
    lbl_out.mkdir(parents=True, exist_ok=True)

    for img_path, lbl_path in pairs:
        shutil.copy2(img_path, img_out / img_path.name)
        shutil.copy2(lbl_path, lbl_out / lbl_path.name)


def write_yaml(output: Path, class_names: list[str]) -> Path:
    nc   = len(class_names)
    names_str = "\n".join(f"  - {n}" for n in class_names)
    yaml = (
        f"# Auto-generated by dataset.py\n"
        f"path: {output.absolute()}\n"
        f"train: train/images\n"
        f"val:   valid/images\n"
        f"test:  test/images\n\n"
        f"nc: {nc}\n"
        f"names:\n{names_str}\n"
    )
    yaml_path = output / "data.yaml"
    yaml_path.write_text(yaml)
    return yaml_path


def prepare_dataset(
    source_dir: str = DEFAULT_SOURCE,
    output_dir: str = DEFAULT_OUTPUT,
    class_names: list[str] = CLASS_NAMES,
    train_ratio: float = TRAIN_RATIO,
    val_ratio: float   = VAL_RATIO,
    seed: int = 42,
) -> str | None:
    """
    Prepare a YOLO-format dataset from raw source images + labels.
    Returns the path to data.yaml on success, or None on failure.
    """
    print("=" * 60)
    print("📊  DATASET PREPARATION")
    print("=" * 60)

    source = Path(source_dir)
    output = Path(output_dir)

    if not source.exists():
        print(f"\n❌  Source not found: {source}")
        print("    Create the folder and place your images + YOLO labels inside.")
        return None

    print(f"\n🔍  Scanning: {source}")
    pairs = find_pairs(source)

    if not pairs:
        print("❌  No valid image-label pairs found.")
        print("    Expected: image files (.jpg/.png) with matching .txt YOLO labels.")
        return None

    print(f"✅  Found {len(pairs)} image-label pairs")

    # Split
    random.seed(seed)
    random.shuffle(pairs)

    n_train = int(train_ratio * len(pairs))
    n_val   = int(val_ratio   * len(pairs))

    train_pairs = pairs[:n_train]
    val_pairs   = pairs[n_train : n_train + n_val]
    test_pairs  = pairs[n_train + n_val :]

    print(f"\n📈  Split:")
    print(f"    Train : {len(train_pairs):>5}  ({len(train_pairs)/len(pairs)*100:.1f}%)")
    print(f"    Valid : {len(val_pairs):>5}  ({len(val_pairs)/len(pairs)*100:.1f}%)")
    print(f"    Test  : {len(test_pairs):>5}  ({len(test_pairs)/len(pairs)*100:.1f}%)")

    # Clear and rebuild output
    if output.exists():
        shutil.rmtree(output)

    print(f"\n📦  Writing to: {output}")
    copy_split(train_pairs, output, "train")
    copy_split(val_pairs,   output, "valid")
    copy_split(test_pairs,  output, "test")

    yaml_path = write_yaml(output, class_names)
    print(f"✅  data.yaml written: {yaml_path}")

    print("\n" + "=" * 60)
    print("✅  Dataset ready.")
    print(f"    Next step:  python main.py --data {yaml_path}")
    print("=" * 60)

    return str(yaml_path)


# ── CLI ───────────────────────────────────────────────────────────────────────
if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Prepare a YOLO dataset from raw images + labels.")
    parser.add_argument("--source",  default=DEFAULT_SOURCE, help="Raw dataset folder")
    parser.add_argument("--output",  default=DEFAULT_OUTPUT, help="Output dataset folder")
    parser.add_argument("--classes", nargs="+", default=CLASS_NAMES,
                        help="Class names in index order, e.g. --classes person vehicle")
    parser.add_argument("--train",   type=float, default=TRAIN_RATIO, help="Train split ratio")
    parser.add_argument("--val",     type=float, default=VAL_RATIO,   help="Val split ratio")
    parser.add_argument("--seed",    type=int,   default=42)
    args = parser.parse_args()

    prepare_dataset(
        source_dir  = args.source,
        output_dir  = args.output,
        class_names = args.classes,
        train_ratio = args.train,
        val_ratio   = args.val,
        seed        = args.seed,
    )